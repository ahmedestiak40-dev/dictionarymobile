// ЁЯФН Search Function
function searchWord() {
  const input = document.getElementById("searchInput").value.trim().toLowerCase();
  const resultBox = document.getElementById("resultBox");
  const speakBtn = document.getElementById("speakBtn");

  if (!input) {
    resultBox.innerHTML = "<p>Please type a word...</p>";
    speakBtn.style.display = "none";
    return;
  }

  const isEnglish = /^[a-zA-Z]+$/.test(input);
  let result;

  if (isEnglish) {
    result = dictionary[input];
    if (result) {
      resultBox.innerHTML = `<strong>${input}</strong> тЖТ <span>${result}</span>`;
      speakBtn.style.display = "inline-block";

      // Ensure speak works on mobile (inside user event)
      speakBtn.onclick = () => speak(input);
      speakBtn.ontouchend = () => speak(input); // Mobile fix
    } else {
      resultBox.innerHTML = `<p>Meaning not found!</p>`;
      speakBtn.style.display = "none";
    }
  } else {
    const found = Object.entries(dictionary).find(([, v]) => v === input);
    if (found) {
      resultBox.innerHTML = `<strong>${input}</strong> тЖТ <span>${found[0]}</span>`;
      speakBtn.style.display = "inline-block";
      speakBtn.onclick = () => speak(found[0]);
      speakBtn.ontouchend = () => speak(found[0]); // Mobile fix
    } else {
      resultBox.innerHTML = `<p>Meaning not found!</p>`;
      speakBtn.style.display = "none";
    }
  }
}

// ЁЯЧг Pronunciation
function speak(word) {
  const utterance = new SpeechSynthesisUtterance(word);
  utterance.lang = "en-US";
  // Cancel any ongoing speech before speaking
  speechSynthesis.cancel();
  speechSynthesis.speak(utterance);
}

